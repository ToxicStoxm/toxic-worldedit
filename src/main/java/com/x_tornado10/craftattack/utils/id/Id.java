package com.x_tornado10.craftattack.utils.id;

public class Id {
    private int x;
    private int y;
    private int z;
    public Id(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
